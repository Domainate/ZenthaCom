function hd_office_hours_list()
{
    ob_start();
    $schedules = officeHoursSchedules();
    echo '<div class="hd-office-hours-grid">';
    foreach ($schedules as $key => $schedule) {
        ?>
        <div id="schedule-<?= $key ?>" class="schedule-card">
            <div class="flex flex-col justify-center items-center w-full" style="min-height: 200px;">
                <?php if ($schedule['video_url']): ?>
                    <!-- Video Embed -->
                    <figure
                        class="wp-block-embed is-type-video w-full h-full is-provider-vimeo wp-block-embed-vimeo wp-embed-aspect-16-9 wp-has-aspect-ratio"
                        style="margin-bottom:12px;">
                        <div class="wp-block-embed__wrapper">
                            <iframe src="<?= $schedule['video_url'] ?>" width="100%" height="180" frameborder="0"
                                allow="autoplay; fullscreen; picture-in-picture" allowfullscreen
                                style="border-radius:6px;width:100%;max-width:100%;"></iframe>
                        </div>
                    </figure>
                <?php endif; ?>
            </div>
            <div class="flex w-full flex-col justify-start items-start">
                <h5><strong><?= $schedule['title'] ?></strong></h5>
                <h6><?= date('F j, Y', strtotime($schedule['date_time'])) ?></h6>
                <h6>Q&A Starts at <?= $schedule['qa_start'] ?></h6>
            </div>
        </div>
    <?php }
    echo '</div>';
    ?>
    <style>
        .hd-office-hours-grid {
            display: grid;
            grid-template-columns: 1fr;
            gap: 24px;
        }

        .schedule-card {
            border: 1px solid #e5e7eb;
            border-radius: 8px;
            padding: 0.5em 1em;
            background: #fff;
            box-shadow: 0 2px 8px rgba(0, 0, 0, 0.04);
            display: flex;
            flex-direction: column;
            align-items: center;
            justify-content: space-center;
            gap: 1em;
        }

        @media (min-width: 600px) {
            .hd-office-hours-grid {
                grid-template-columns: repeat(2, 1fr);
            }
        }

        @media (min-width: 1024px) {
            .hd-office-hours-grid {
                grid-template-columns: repeat(3, 1fr);
            }
        }

        @media (max-width: 599px) {
            .schedule-card {
                flex-direction: column;
                align-items: flex-start;
            }
        }
    </style>
    <?php
    return ob_get_clean();
}
